package implementarDiagramas;

public abstract class Animal {
	
	private String nome;
	private String raca;
	
	public Animal (String nome, String raca) {
		
	}
	public String getNome() {
		return nome;
	}
	
	public String getRaca() {
		return raca;
	}
	
	public void caminha(String caminha) {}
	
	

}
